    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        try{
        String placaSeleccionada = (String) jComboBox1.getSelectedItem();

        if (placaSeleccionada== null||placaSeleccionada.isEmpty()) {
            txtPrecio.setText("");
            txtTotal.setText("");
            return;
        }

        IVehiculoDatos vehiculosDatos = new VehiculosDatos();
        int idVehiculo = vehiculosDatos.obtenerIdPorPlaca(placaSeleccionada);
        double precio = vehiculosDatos.obtenerPrecioPorId(idVehiculo);
        txtPrecio.setText(String.format("%.2f",precio));
        
        if (!txtDiasRentados.getText().isEmpty()) {
            int dias = Integer.parseInt(txtDiasRentados.getText());
            double total = dias * precio;
            txtTotal.setText(String.format("%.2f", total));
        }

    }catch(Exception e){
        System.out.println("Error al obtener el precio del vehículo: " + e.getMessage());
    }
    }//GEN-LAST:event_jComboBox1ActionPerformed

