private void cargarPlacas() {
    IVehiculoDatos vehiculosDatos = new VehiculosDatos();
    jComboBox1.removeAllItems();
    jComboBox1.addItem("");
    for (String placa : vehiculosDatos.getPlacas()) {
        jComboBox1.addItem(placa);
    }
}
private void calcularDiasRentados() {
    try{
        String fechaInicioTxt=txtFechaInicio.getText().trim();
        String fechaFinTxt=txtFechaFin.getText().trim();

        if(fechaInicioTxt.isEmpty()||fechaFinTxt.isEmpty()) {
            return;
        }

        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");

        java.util.Date fechaInicio = sdf.parse(fechaInicioTxt);
        java.util.Date fechaFin = sdf.parse(fechaFinTxt);

        java.sql.Date fechaInicioSQL = new java.sql.Date(fechaInicio.getTime());
        java.sql.Date fechaFinSQL = new java.sql.Date(fechaFin.getTime());

        LocalDate inicio = fechaInicioSQL.toLocalDate();
        LocalDate fin = fechaFinSQL.toLocalDate();

        int dias =(int) ChronoUnit.DAYS.between(inicio,fin);

        if (dias >= 0) {
            txtDiasRentados.setText(String.valueOf(dias));
        } else {
            txtDiasRentados.setText("0");
        }

    }catch(Exception e){
        System.out.println("Error al calcular días rentados: " + e.getMessage());
    }
}

