private void cargarTablaVehiculos(){

    IVehiculoDatos vehiculoDatos = new VehiculosDatos();
    List<Vehiculo> vehiculos;

    vehiculos = vehiculoDatos.getVehiculosAlquilados();
    model.setRowCount(0);
    for(Vehiculo vehiculo :vehiculos ){
        Object[] llenar = new Object[]{
            vehiculo.getId_vehiculo(),
            vehiculo.getMarca(),
            vehiculo.getModelo(),
            vehiculo.getAnio(),
            vehiculo.getPlaca(),
            vehiculo.getEstado(),
            vehiculo.getCosto_diario(),
        };

        model.addRow(llenar);
    }
    tablaVehiculos.setModel(model);

}
private void modificarVehiculo(){
    int id = Integer.parseInt(this.txtID.getText());
    if (id == -1 ){
        JOptionPane.showMessageDialog(this, "Seleccione un vehiculo");
        return;
    }
    try{

        IAlquilerDatos alquileres = new AlquilerDatos();
        Vehiculo vehiculo = new Vehiculo ();
        IVehiculoDatos service = new VehiculosDatos();
        int idGeneral = id;
        String estado = this.txtEstado.getText();
        alquileres.cambiarFinalizado(idGeneral);

        service.actualizarEstadoVehiculo(idGeneral, estado);
        cargarTablaVehiculos();
        JOptionPane.showMessageDialog(this, "Se modifico correctamente el vehiculo de marca ");

    }catch(Exception e){
        System.out.println("ha ocurrido un error" + e.getMessage());
    }

}

